"""
Инструменты для запуска приложений.
"""

from .mcp_core import mcp


@mcp.tool
def open_app(app_name: str) -> str:
    """
    Запускает системное приложение Windows по его имени.

    Эта функция позволяет запускать стандартные приложения Windows, такие как калькулятор,
    блокнот, проводник и другие программы, которые зарегистрированы в системе.
    Не вызывай эту функцию если требуется открыть папку.

    Args:
        app_name (str): Имя исполняемого файла приложения без расширения .exe.
                       Примеры: 'calc', 'notepad', 'explorer', 'mspaint', 'cmd', 'powershell'.

    Returns:
        str: Сообщение об успешном запуске или описание ошибки.

    Examples:
        open_app('calc') -> "Команда на запуск 'calc' отправлена."
        open_app('notepad') -> "Команда на запуск 'notepad' отправлена."

    Note:
        Функция использует subprocess.Popen с shell=True для запуска приложений.
        Приложение запускается асинхронно и не блокирует выполнение скрипта.
    """
    # Возвращаем СПЕЦИАЛЬНУЮ команду, которую перехватит main.py
    return f"__OPEN_APP_COMMAND__:{app_name}"
